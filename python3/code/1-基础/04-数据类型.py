# int -- 整型
num1 = 1
print(type(num1))

# float -- 浮点型
num2 = 1.1
print(type(num2))

# str -- 字符串
a = 'hello world'
print(type(a))

# bool -- 布尔型
b = True
print(type(b))
# print(type(False))

# list -- 2.列表
c = [10, 20, 30]
print(type(c))

# tuple -- 3.元组
d = (10, 20, 30)
print(type(d))

# set -- 5.2-集合类型
e = {10, 20, 30}
print(type(e))

# dict -- 字典
f = {'name': 'TOM', 'age': 18}
print(type(f))
